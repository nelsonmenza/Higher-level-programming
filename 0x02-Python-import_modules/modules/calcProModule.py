"""Write a function that sum, sub, mul and div and return the result.
    -Create a module to import each function and print examples of each function"""

"""A module that contains functions for performing basic arithmetic operations."""


def sum(a, b, op):
    # Adds two numbers and returns the result.
    return a + b


def sub(a, b, op):
    # Subtracts one number from another and returns the result.
    return a - b


def mult(a, b):
    # Multiplies two numbers and returns the result.
    return a * b


def div(a, b):
    # Divides one number by another and returns the result.
    return a / b
